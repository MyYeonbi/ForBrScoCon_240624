public class Q1_Count {
    //처음 10개의 자연수를 출력하는 프로그램을 작성해라.
    //이때 count라는 변수를 사용해야 한다.
    //while문 , for문  2가지 버전의 정답을 만들어라.

    public static void main(String[] args) {
        int count = 1;

        while(count <= 10){
            System.out.println(count);
            count++;
        }

        for(int countt =1; countt <=10; countt++){
            System.out.println(countt);
        }


    }
}
